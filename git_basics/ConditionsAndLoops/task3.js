// for loop - multiplication table

const number = 5;

console.log('This is for loop:')
for (let i=1; i <= 10; i++) {
    console.log(number + ' x ' + i + ' = ' + number * i)
}

// while loop - multiplication table

console.log('This is while loop:')
let count = 1
while (count <= 10) {
    console.log(number + ' x ' + count + ' = ' + number * count)
    count++
}